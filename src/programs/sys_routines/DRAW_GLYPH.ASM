	CONST	GLYADDR	0x100
	CONST	GLYOUT1	0x101
	CONST	GLYOUT2	0x102
	CONST	GLYOUT3	0x103
	
	PTR		DISPRAM	0xD0
	
	% In: 
	%	r1 = ASCII
	%	r2 = OFFSET_IN_DISPRAM
DRAW_GLYPH:
	OUTCSR	r1	GLYADDR
	PUSH	r1
	INCSR	r1	GLYOUT1
	ISTORE	r1	r2(DISPRAM + 0)
	INCSR	r1	GLYOUT2
	ISTORE	r1	r2(DISPRAM + 2)
	INCSR	r1	GLYOUT3
	ISTORE	r1	r2(DISPRAM + 4)
	POP		r1
	RET
	